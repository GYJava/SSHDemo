package com.servlet;

public class OznerUser {
	
	private String OzneUser;
	
	private String UserName;
	
	private String id;
	
	private String pwd;

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUserName() {
		return UserName;
	}

	public void setUserName(String userName) {
		UserName = userName;
	}

	public String getOzneUser() {
		return OzneUser;
	}

	public void setOzneUser(String ozneUser) {
		OzneUser = ozneUser;
	}

}
